                        <div class="col-auto text-center py-3">
                            <h1>
                                Guía de mudanza y consejos para embalar
                            </h1>
                            <h3>Ultima actualizacion 31/10/2023</h3>
                        </div>
                        <article  id="intro">
                            Ya sea que se mude al otro lado del país o al final de la calle, alquilar una unidad de
                            almacenamiento antes de mudarse le brindará el espacio que necesita no solo para guardar
                            las cosas, sino también para desempacar e instalarse en su propio tiempo. Trashtero.es
                            lo ayudará a prepararse para mudarse a una unidad de almacenamiento con nuestros consejos de expertos.
                        </article>
                        <br>
                        <article id="1">
                            <h4>
                                <b>
                                    Consejos para embalar
                                </b>
                            </h4>
                            <ol>
                                <li>
                                    Embale primero los artículos menos usados.
                                </li>
                                <li>
                                    Organiza cajas por habitación y etiqueta todo.
                                </li>
                                <li>
                                    Rompe los muebles tanto como sea posible.
                                </li>
                                <li>
                                    Embale las cajas con poco o ningún espacio para evitar que se muevan.
                                </li>
                                <li>
                                    Embale los productos electrónicos en cajas especialmente diseñadas.
                                </li>
                                <li>
                                    Invierta en fundas para sofás y colchones.
                                </li>
                                <li>
                                    Embale y llene con cuidado los artículos de vidrio huecos con espuma o papel.
                                </li>
                                <li>
                                    Embale los artículos de limpieza y otros líquidos en bolsas de plástico selladas.
                                </li>
                                <li>
                                    Lleve consigo objetos de valor como registros en papel, documentos financieros y joyas.
                                </li>
                                <li>
                                    Cargue el camión primero con los artículos más pesados.
                                </li>
                                <li>
                                    Embale una caja para pasar la noche para su nuevo lugar que incluya artículos de tocador, ropa de cama,
                                    recetas, etc.
                                </li>
                            </ol>
                        </article>
                        <article id="2">
                            <h4>
                                <b>
                                    Guía de mudanzas
                                </b>
                            </h4>
                            <h6>
                                <b>
                                    Cosas que hacer dos meses antes:
                                </b>
                            </h6>
                            <ul>
                                <li>
                                    Comience alquilando su unidad de almacenamiento y revisando todos los artículos de su casa. Querrá saber de antemano qué se queda, qué se va y qué se puede pasar fácilmente en su unidad de almacenamiento sin que se pierda.
                                </li>
                                <li>
                                    Guarde todos sus recibos a efectos fiscales.
                                </li>
                                <li>
                                    Conéctese en línea y comience a buscar farmacias, bancos, supermercados y tiendas de conveniencia cerca de su nueva casa.
                                </li>
                                <li>
                                    Haga lo mismo con las empresas de mudanzas y comience a recibir cotizaciones.
                                </li>
                                <li>
                                    Haga una lista de sus artículos más caros y llame a la compañía de seguros de su propietario para que los cubra durante la transición. También puede informarles su nueva dirección y fijar una fecha para el cambio de política.
                                </li>
                                <li>
                                    De hecho, haga una lista de todos los lugares donde necesita proporcionar una dirección actualizada.
                                </li>
                                <li>
                                    Si abandona alguna de estas instituciones, obtenga una copia de sus registros. Las cuentas bancarias y de salud son particularmente importantes. Mientras los llama, programe citas de último momento. Probablemente se quedará sin médico o dentista si se dirige a un área completamente nueva.
                                </li>
                                <li>
                                    Configure todos sus planes de viaje: quién tomará qué automóviles, aviones, trenes o taxis.
                                </li>
                            </ul>
                        </article >
                        <article id="3">
                            <h6>
                                <b>
                                    Cosas que hacer dos semanas antes:
                                </b>
                            </h6>
                            <ul>
                                <li>
                                    Comience a limpiar las habitaciones casi vacías. Friegue electrodomésticos, zócalos, paredes, techos y cualquier tratamiento de pared que vaya a dejar atrás.
                                </li>
                                <li>
                                    Continúe empacando hasta llegar a lo absolutamente esencial.
                                </li>
                                <li>
                                    Comienza a armar tu caja para pasar la noche con lo esencial.
                                </li>
                                <li>
                                    Comience a realizar copias de seguridad de las computadoras y prepárelas para la mudanza.
                                </li>
                                <li>
                                    Si utiliza una empresa de mudanzas, llámelos para confirmar la fecha y la hora. Consígales direcciones impresas y su número de celular.
                                </li>
                            </ul>
                        </article>
                        <article id="4">
                            <h6>
                                <b>
                                    Cosas que hacer una semana antes:
                                </b>
                            </h6>
                            <ul>
                                <li>
                                    Debe saber si el cierre se llevará a cabo según lo programado y comenzar a preparar las llaves, los abridores de puertas de garaje y los códigos de alarma.
                                </li>
                                <li>
                                    Realice su recorrido final en el nuevo lugar aproximadamente 3 días antes del cierre.
                                </li>
                                <li>
                                    Termina de empacar, pero deja un par de conjuntos de ropa en bolsas de papel en caso de que haya una emergencia y los camiones de mudanzas no aparezcan.
                                </li>
                                <li>
                                    Es hora de preparar sus electrodomésticos, así que retire los materiales inflamables como los tanques de propano y limpie todo.
                                </li>
                                <li>
                                    No olvides llevar algo de dinero en efectivo si necesitas darle propina a la empresa de mudanzas. Cuesta unos 20 dólares por persona.
                                </li>
                                <li>
                                    Limpia el refrigerador el día antes de que comience tu mudanza.
                                </li>
                                <li>
                                    Haga previamente carteles en trozos de papel que etiqueten cada puerta de la casa. Cuando llegues a la nueva casa, cuelga los carteles en las puertas correspondientes.
                                </li>
                            </ul>
                        </article>
                        <article id="5">
                            <h6>
                                <b>
                                    Cosas que hacer el día de la mudanza:
                                </b>
                            </h6>
                            <ul>
                                <li>
                                    Empaquétalo todo y, cuando parezca que todo está listo, revisa la casa por última vez.
                                </li>
                                <li>
                                    Dirígete al nuevo lugar e inmediatamente prepara los artículos de tocador, las bolsas de basura y la ropa de cama.
                                </li>
                                <li>
                                    Recorra minuciosamente el lugar y cuelgue todos los carteles de su habitación.
                                </li>
                                <li>
                                    Verifique los detectores de incendios para asegurarse de que todavía estén funcionando.
                                </li>
                                <li>
                                    Considere colocar plástico para proteger el piso. Cuando todos lleguen, dales un breve recorrido por la casa para que se aclimaten.
                                </li>
                                <li>
                                    Tan pronto como los encargados de la mudanza hayan terminado, si aún no lo han hecho, instale las camas. Hacer esto ahora te hará muy feliz de tener un lugar donde caer al final del día.
                                </li>
                            </ul>
                        </article>
